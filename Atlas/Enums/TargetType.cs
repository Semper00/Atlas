        /// Represents a sport target.
        /// </summary>
        SportTarget,

        /// <summary>
        /// Represents a Class-D target.
        /// </summary>
        ClassDTarget,

        /// <summary>
        /// Represents a binary target.
        /// </summary>
        BinaryTarget
    }
}
