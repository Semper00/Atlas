		/// </summary>
		LowRange,

		/// <summary>
		/// Medium radio range.
		/// </summary>
		MediumRange,

		/// <summary>
		/// High radio range.
		/// </summary>
		HighRange,

		/// <summary>
		/// Ultra radio range.
		/// </summary>
		UltraRange
	}
}